export function buildV60PreCostRows(offset: number): CostDictionaryPresetRow[] {
  return groups.flatMap((group) => group.details.map(([code, detail], detailIndex) => ({
    ...common,
    rowIndex: offset + groups.slice(0, groups.indexOf(group)).reduce((sum, item) => sum + item.details.length, 0) + detailIndex,
    costCode: code,
    parentCode: group.code,
    subjectLevel: '4',
    firstSubject: '前期工程费',
    secondSubject: group.section,
    thirdSubject: group.group,
    detailSubject: detail,
    subjectDefinition: `${detail}，来源于V60前期费用明细表B列明细项目，用于目标成本明细测算。`,
    targetMappingCode: code,
    measureBasis: group.measureBasis,
    unit: group.unit,
    defaultTaxRate: group.tax,
    applicableProductType: '项目整体共用',
    remark: group.remark || 'V60前期费明细科目，按合同、政府文件、专项成果和财税审核复核。',
    landVatAllocationMethod: common.landVatAllocationMethod,
    incomeTaxDeductionCategory: common.incomeTaxDeductionCategory
  })));
}
